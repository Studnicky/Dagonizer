#!/usr/bin/env node
/**
 * build-palette.mjs: runs the iridis CLI against the docs palette
 * config, then unwraps its JSON output (rootBlock + scopedBlock + …)
 * into a single raw `iridis.palette.css`. The generated CSS is what
 * `palette.css` then `@imports`.
 *
 * Why a wrapper: the `emit:cssVars` task returns an object with several
 * blocks (`rootBlock`, `scopedBlock`, `darkScheme`, `forcedColors`,
 * `wideGamut`, `full`). The CLI's OutputWriter JSON-stringifies non-string
 * outputs, so the file lands as JSON. This script reads that JSON and
 * writes the `full` member as `.css`.
 *
 * Syntax highlighting itself uses VitePress's bundled Shiki theme
 * (`night-owl` in config.ts): well-tested, navy-grounded, contrast-
 * verified, and aligned with the talisman aesthetic. The code-block
 * CHROME (frame, padding, font) is themed via CSS using the iridis
 * palette tokens generated here. We do not ship a custom Shiki theme.
 *
 * Run via:  npm run docs:theme
 */

import { spawnSync } from 'node:child_process';
import { readFileSync, writeFileSync, existsSync, unlinkSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE       = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT  = resolve(HERE, '..', '..', '..');
const CONFIG     = resolve(HERE, '..', 'iridis.config.json');
const RAW_OUTPUT = resolve(HERE, '..', 'theme', 'iridis.palette.json');
const FINAL_CSS  = resolve(HERE, '..', 'theme', 'iridis.palette.css');
const IRIDIS_BIN = resolve(REPO_ROOT, '..', 'iridis', 'node_modules', '.bin', 'iridis');

if (!existsSync(IRIDIS_BIN)) {
  // CI builds (and any environment without a sibling iridis workspace
  // checkout) skip regeneration and use the committed iridis.palette.css.
  // The generated CSS is the source of truth for the docs site; the iridis
  // CLI is only needed when changing iridis.config.json. Hard-fail only
  // when the committed CSS is also missing.
  if (existsSync(FINAL_CSS)) {
    console.log(`iridis CLI not found at ${IRIDIS_BIN}; using committed ${FINAL_CSS}`);
    process.exit(0);
  }
  console.error(`iridis CLI not found at ${IRIDIS_BIN} and no committed ${FINAL_CSS}`);
  console.error('Install the iridis workspace first, or commit a fallback iridis.palette.css.');
  process.exit(1);
}

const result = spawnSync(IRIDIS_BIN, [CONFIG], {
  cwd: REPO_ROOT,
  stdio: ['ignore', 'inherit', 'inherit'],
});

// Tolerate iridis CLI failures (slot-name drift, config schema changes,
// missing output) when the committed CSS exists. The committed file is
// the source of truth for the docs site; regeneration is a developer
// convenience, not a build requirement.
if (result.status !== 0 || !existsSync(RAW_OUTPUT)) {
  if (existsSync(FINAL_CSS)) {
    console.log(`iridis CLI exited ${result.status ?? 'with no output'}; using committed ${FINAL_CSS}`);
    process.exit(0);
  }
  if (result.status !== 0) process.exit(result.status ?? 1);
  console.error(`expected iridis output at ${RAW_OUTPUT}; none written, and no committed ${FINAL_CSS} fallback`);
  process.exit(1);
}

const raw = JSON.parse(readFileSync(RAW_OUTPUT, 'utf8'));
const css = raw.full ?? raw.rootBlock ?? '';

if (!css) {
  console.error('iridis output had no `full` or `rootBlock` block');
  process.exit(1);
}

const header = [
  '/**',
  ' * iridis.palette.css: generated by docs/.vitepress/scripts/build-palette.mjs',
  ' *',
  ' * DO NOT EDIT BY HAND. Edit docs/.vitepress/iridis.config.json and run',
  ' *   npm run docs:theme',
  ' * to regenerate. Every token is WCAG 2.1 AAA verified by `enforce:wcagAAA`.',
  ' */',
  '',
].join('\n');

writeFileSync(FINAL_CSS, `${header}${css}\n`, 'utf8');
unlinkSync(RAW_OUTPUT);

console.log(`wrote ${FINAL_CSS}`);
